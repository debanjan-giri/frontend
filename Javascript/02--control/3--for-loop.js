// for loop
// execute a block of code multiple times
for (let i = 0; i < 5; i++) {
    console.log(i); // 0 1 2 3 4
  }
  
  // for in
  // it will print key for array and object
  let data = [10, 20, 30, 40];
  for (let key in data) {
    console.log(key); // 0 1 2 3
  }
  
  // for of
  // it will print array value
  let data2 = [10, 20, 30, 40];
  for (let value of data2) {
    console.log(value); // 10 20 30 40
  }
  