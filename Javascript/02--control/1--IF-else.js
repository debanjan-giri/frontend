// if condition
if (true) {
  console.log("hello");
}

// else
if (false) {
  console.log("");
} else {
  console.log("hello");
}

// else if
if (false) {
  console.log("");
} else if (true) {
  console.log("hello");
} else {
  console.log("");
}

// nested if
if (true) {
  if (false) {
    console.log("");
  } else {
    console.log("inner");
  }
}
