// switch case
switch (78) {
    case 8:
      console.log("");
      break;
    case 78:
      console.log("hello");
      break;
    default:
      console.log("");
      break;
  }
  
  // nested switch case
  switch (78) {
    case 78:
      switch (78) {
        case 8:
          console.log("");
          break;
        default:
          console.log("hello");
          break;
      }
      break;
    default:
      console.log("");
      break;
  }
  